<?php
 // Primer ejemplo de funciones en PHP.
 // En este ejemplo creamos una función sencilla que muestra un texto por pantalla.

  function mostrarTexto ()
  {
  echo ("Texto que se muestra en la página");
  }
  // A continuación llamamos a la función anterior para que muestre el texto por pantalla.
  mostrarTexto();
 
  // Ejemplo de funciones en php en el que devuelve un contenido o valor con return
 
  function mostrarTextor ()
  {
  return ("Texto que se muestra en la página");
  }
   // A continuación llamamos a la función anterior para que muestre el texto por pantalla.
  echo mostrarTextor();
 ?>